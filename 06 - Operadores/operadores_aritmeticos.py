#operadores aritméticos

a = 10
b = 3

# suma
suma = a + b
print(f'La suma es : {suma}')

#resta
resta = a - b
print(f'La resta es : {resta}')

#Multiplicación
multiplicacion = a * b
print(f'La multiplicación es : {multiplicacion}')

#diviisón
division = a / b
print(f'División {division:.2}')

#división entera
division_entera = a // b
print(f'La división entera es : {division_entera}')

#Modulo (%) residu
modulo = a % b
print(f'El residuo es : {modulo}')

#Exponente **
exponente = a ** b #10 elevado a 3
print(f'Potencia o exponente es : {exponente}')
