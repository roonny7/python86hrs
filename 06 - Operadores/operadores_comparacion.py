print('*** Operadores de comparaciÃ³n (Relacionales) ***')

a,b = 5,5
print(f'Valor inicial a : {a}, b : {b}')

#Operador igualidad ==
resultado = a == b
print(f'Resultado de evaluar a == b es : {resultado}')

#Operador diferente !=
resultado = a != b
print(f'Resultado de evaluar a != b es : {resultado}')

#Operador mayor que ><
resultado = a > b
print(f'Resultado de evaluar a > b es : {resultado}')

#Operador mayor o igual que ><
resultado = a >= b
print(f'Resultado de evaluar a >= b es : {resultado}')


#Operador menor que <
resultado = a < b
print(f'Resultado de evaluar a < b es : {resultado}')

#Operador mayor o igual que ><
resultado = a <= b
print(f'Resultado de evaluar a <= b es : {resultado}')